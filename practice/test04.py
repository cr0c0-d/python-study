# 타입캐스팅

num1 = input('첫번째 숫자를 입력하세요')
num2 = input('두번째 숫자를 입력하세요')
print(num1 + '+' + num2 + '는 ' + num1 + num2 + ' 입니다.') ## String끼리 합쳐짐!

# int로 타입캐스팅
sum = int(num1) + int(num2)
print(num1 + '+' + num2 + '는 ' + str(sum) + ' 입니다.') # 정수로 형변환해서 결과 계산 -> 문자열로 형변환해서 출력

# float() : 정수형
# ord(문자) : 문자 -> 아스키코드
# chr(아스키코드) : 아스키코드 -> 문자






